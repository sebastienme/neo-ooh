function hideSeeMore() {
    let lastDivChildHeight = getComputedStyle(lastDivChild).getPropertyValue('height');

    if (lastDivChildHeight != "0px") {
        seeMoreDiv.innerHTML = "";
        console.log(lastDivChildHeight)
    }
}

